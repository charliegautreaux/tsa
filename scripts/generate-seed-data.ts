import fs from "fs";
import path from "path";

interface TzBox { tz: string; w: number; s: number; e: number; n: number }

const TZ_PATH = path.join(__dirname, "../src/data/tz-lookup.json");
const SEED_PATH = path.join(__dirname, "../src/data/airports-seed.json");

const tzBoxes: TzBox[] = JSON.parse(fs.readFileSync(TZ_PATH, "utf-8"));

function lookupTz(lat: number, lng: number): string {
  for (const b of tzBoxes) {
    if (lat >= b.s && lat <= b.n && lng >= b.w && lng <= b.e) return b.tz;
  }
  return "America/New_York";
}

function icao(iata: string, state: string): string {
  if (["HI"].includes(state)) return `PH${iata.slice(-2)}`;
  if (["AK"].includes(state)) return `PA${iata.slice(-2)}`;
  if (["GU","AS","MP"].includes(state)) return `PG${iata.slice(-2)}`;
  if (["PR","VI"].includes(state)) return `TJ${iata.slice(-2)}`;
  return `K${iata}`;
}

